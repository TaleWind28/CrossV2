package Utils;

public class AnsiColors {
    public static final String RESET = "\u001B[0m";

    // 6 tonalità di BLU (colore principale)
    public static final String BLUE_DARK = "\u001B[34m";        // Blu base
    public static final String BLUE_MEDIUM = "\u001B[94m";      // Blu chiaro
    public static final String BLUE_LIGHT = "\u001B[96m";       // Ciano chiaro
    public static final String BLUE_ULTRA_LIGHT = "\u001B[97m"; // Bianco con tono bluastro
    public static final String BLUE_VIBRANT = "\u001B[36m";     // Ciano base
    public static final String BLUE_DEEP = "\u001B[35m";        // Magenta-bluastro

    // 4 tonalità di VERDE (colore secondario)
    public static final String GREEN_DARK = "\u001B[32m";      // Verde base
    public static final String GREEN_MEDIUM = "\u001B[92m";    // Verde brillante
    public static final String GREEN_LIGHT = "\u001B[96m";     // Verde acqua chiaro
    public static final String GREEN_VIBRANT = "\u001B[93m";   // Verde tendente al giallo
    public static final String MAGENTA = "\u001B[35m";          //magenta

    public static final String BRIGHT_RED = "\u001B[91m"; //rosso chiaro

    // 1 colore completamente diverso: ARANCIONE
    public static final String ORANGE = "\u001B[38;5;214m";     // Arancione ANSI (RGB 214)

    public static final String RED = "\u001B[31m";

    
}

